#include "3-calc.h"

/**
  * op_add - function to return sum of two integers
  * @a: first number
  * @b: second number
  * Return: Sum
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - function to subtract integers
  * @a: first number
  * @b: second number
  * Return: Subtracted integers
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - function to multiply
  * @a: first number
  * @b: second number
  * Return: Multiplied numbers
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - function to divide integers
  * @a: first number
  * @b: second number
  * Return: Divided numbers
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - function to mod integers
  * @a: first number
  * @b: second number
  * Return: Remainder of number
  */
int op_mod(int a, int b)
{
	return (a % b);
}
